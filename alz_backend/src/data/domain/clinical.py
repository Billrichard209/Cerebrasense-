class SymptomLog(BaseModel):
    """Structured symptom log for dashboard trend display."""

    model_config = ConfigDict(extra="forbid")

    symptom_log_id: UUID = Field(default_factory=uuid4)
    patient_id: UUID
    symptom_name: str
    severity: int = Field(ge=0, le=10)
    observed_at: datetime
    reported_by_id: UUID | None = None
    reported_by_role: str | None = None
    notes: str | None = None
    audit: AuditFields = Field(default_factory=AuditFields)
    metadata: dict[str, Any] = Field(default_factory=dict)


class AlertRecord(BaseModel):
    """Care-team alert generated by users, rules, or model/longitudinal outputs."""

    model_config = ConfigDict(extra="forbid")

    alert_id: UUID = Field(default_factory=uuid4)
    patient_id: UUID
    alert_type: str
    priority: AlertPriority = AlertPriority.moderate
    status: AlertStatus = AlertStatus.open
    title: str
    message: str
    generated_at: datetime
    generated_by: str = Field(description="Examples: nurse, clinician, longitudinal_engine, model_inference.")
    related_scan_id: UUID | None = None
    related_report_uri: str | None = None
    acknowledged_by: UUID | None = None
    acknowledged_at: datetime | None = None
    resolved_by: UUID | None = None
    resolved_at: datetime | None = None
    audit: AuditFields = Field(default_factory=AuditFields)
    metadata: dict[str, Any] = Field(default_factory=dict)

    @field_validator("message")
    @classmethod
    def _avoid_diagnosis_language(cls, value: str) -> str:
        """Keep generated alerts compatible with decision-support wording."""

        lowered = value.lower()
        forbidden = ("diagnosed with", "has alzheimer")
        if any(term in lowered for term in forbidden):
            raise ValueError("Alert message must avoid diagnosis claims.")
        return value

